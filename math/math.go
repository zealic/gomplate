package math

// AddInt -
func AddInt(x, y int64) int64 {
	return x + y
}

// SubInt -
func SubInt(x, y int64) int64 {
	return x - y
}

// MultInt -
func MultInt(x, y int64) int64 {
	return x * y
}

// DivInt -
func DivInt(x, y int64) int64 {
	return x / y
}

// AddFloat -
func AddFloat(x, y float64) float64 {
	return x + y
}

// SubFloat -
func SubFloat(x, y float64) float64 {
	return x - y
}

// MultFloat -
func MultFloat(x, y float64) float64 {
	return x * y
}

// DivFloat -
func DivFloat(x, y float64) float64 {
	return x / y
}
